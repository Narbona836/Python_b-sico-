# Operadores aritméticos

soma = 10 + 5
subtracao = 10 - 5
multiplicacao = 10 * 5
divisao = 10 / 5
resto = 10 % 3

print(soma, subtracao, multiplicacao, divisao, resto)

# Operadores de comparação (retornam True ou False)
print(10 < 5)
print(10 > 5)
print(10 == 5)
print(10 != 5)

# Operadores lógicos

print(True and True)
print(True and False)
print(True or False)
print(not True)